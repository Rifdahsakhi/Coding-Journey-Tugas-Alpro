angka = int (input ("Masukkan angka: "))
if angka == 1 :
    print ("Senin")
elif angka == 2:
    print ("Selasa")
elif angka == 3 :
    print ("Rabu")
elif angka == 4 :
    print ("Kamis")
elif angka == 5 :
    print ("Jumat")
elif angka == 6 :
    print ("Sabtu")
elif angka == 7 :
    print ("Minggu")
else :
    print ("input tidak valid")